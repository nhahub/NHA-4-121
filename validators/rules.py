# V4 - schema fields and forbidden age
# ---------------------------------------------------------------------------


def validate_v4_required_fields(patient: PatientRecord) -> list[ValidationIssue]:
    patient_id = _patient_id(patient)
    issues: list[ValidationIssue] = []

    issues.extend(_require_fields(patient, REQUIRED_TOP_LEVEL_FIELDS, patient_id, "V4", ""))

    demographics = patient.get("demographics", {})
    if isinstance(demographics, Mapping):
        issues.extend(_require_fields(demographics, REQUIRED_DEMOGRAPHICS_FIELDS, patient_id, "V4", "demographics"))
        for forbidden in FORBIDDEN_DEMOGRAPHICS_FIELDS:
            if forbidden in demographics:
                issues.append(_issue(
                    "V4",
                    FAIL,
                    patient_id,
                    f"Forbidden demographics field present: {forbidden}.",
                    f"demographics.{forbidden}",
                ))
    else:
        issues.append(_issue("V4", FAIL, patient_id, "demographics must be an object.", "demographics"))

    metadata = patient.get("metadata", {})
    if isinstance(metadata, Mapping):
        issues.extend(_require_fields(
            metadata,
            REQUIRED_PATIENT_METADATA_FIELDS_V17_LITE,
            patient_id,
            "V4",
            "metadata",
        ))
    else:
        issues.append(_issue("V4", FAIL, patient_id, "metadata must be an object.", "metadata"))

    if not isinstance(patient.get("conditions"), Sequence) or isinstance(patient.get("conditions"), (str, bytes)):
        issues.append(_issue("V4", FAIL, patient_id, "conditions must be a list of condition strings.", "conditions"))

    if not isinstance(patient.get("allergy_registry"), Sequence) or isinstance(patient.get("allergy_registry"), (str, bytes)):
        issues.append(_issue("V4", FAIL, patient_id, "allergy_registry must be a list.", "allergy_registry"))

    if not isinstance(patient.get("visits"), Sequence) or isinstance(patient.get("visits"), (str, bytes)):
        issues.append(_issue("V4", FAIL, patient_id, "visits must be a list.", "visits"))
        return issues

    for allergy_index, allergy in enumerate(_allergies(patient)):
        issues.extend(_require_fields(
            allergy,
            REQUIRED_ALLERGY_FIELDS,
            patient_id,
            "V4",
            f"allergy_registry[{allergy_index}]",
        ))

    for visit_index, visit in enumerate(_visits(patient)):
        visit_path = f"visits[{visit_index}]"
        issues.extend(_require_fields(visit, REQUIRED_VISIT_FIELDS, patient_id, "V4", visit_path))

        for forbidden in FORBIDDEN_VISIT_FIELDS:
            if isinstance(visit, Mapping) and forbidden in visit:
                issues.append(_issue(
                    "V4",
                    FAIL,
                    patient_id,
                    f"Forbidden visit field present: {forbidden}.",
                    f"{visit_path}.{forbidden}",
                ))

        vitals = visit.get("vitals", {})
        if isinstance(vitals, Mapping):
            issues.extend(_require_fields(vitals, REQUIRED_VITAL_FIELDS, patient_id, "V4", f"{visit_path}.vitals"))
        else:
            issues.append(_issue("V4", FAIL, patient_id, "vitals must be an object.", f"{visit_path}.vitals"))

        soap_note = visit.get("soap_note", {})
        if isinstance(soap_note, Mapping):
            issues.extend(_require_fields(soap_note, SOAP_SECTIONS, patient_id, "V4", f"{visit_path}.soap_note"))
        else:
            issues.append(_issue("V4", FAIL, patient_id, "soap_note must be an object.", f"{visit_path}.soap_note"))

        clinical_event = visit.get("clinical_event", {})
        if isinstance(clinical_event, Mapping):
            issues.extend(_require_fields(
                clinical_event,
                REQUIRED_CLINICAL_EVENT_FIELDS,
                patient_id,
                "V4",
                f"{visit_path}.clinical_event",
            ))
        else:
            issues.append(_issue("V4", FAIL, patient_id, "clinical_event must be an object.", f"{visit_path}.clinical_event"))

        retrieval_context = visit.get("retrieval_context", {})
        if isinstance(retrieval_context, Mapping):
            issues.extend(_require_fields(
                retrieval_context,
                REQUIRED_RETRIEVAL_CONTEXT_FIELDS,
                patient_id,
                "V4",
                f"{visit_path}.retrieval_context",
            ))
        else:
            issues.append(_issue("V4", FAIL, patient_id, "retrieval_context must be an object.", f"{visit_path}.retrieval_context"))

        if not isinstance(visit.get("labs", []), Sequence) or isinstance(visit.get("labs", []), (str, bytes)):
            issues.append(_issue("V4", FAIL, patient_id, "labs must be a list.", f"{visit_path}.labs"))
        else:
            for lab_index, lab in enumerate(_labs(visit)):
                issues.extend(_require_fields(
                    lab,
                    REQUIRED_LAB_FIELDS,
                    patient_id,
                    "V4",
                    f"{visit_path}.labs[{lab_index}]",
                ))

        if not isinstance(visit.get("medications", []), Sequence) or isinstance(visit.get("medications", []), (str, bytes)):
            issues.append(_issue("V4", FAIL, patient_id, "medications must be a list.", f"{visit_path}.medications"))
        else:
            for med_index, medication in enumerate(_medications(visit)):
                issues.extend(_require_fields(
                    medication,
                    REQUIRED_MEDICATION_FIELDS,
                    patient_id,
                    "V4",
                    f"{visit_path}.medications[{med_index}]",
                ))

    return issues


