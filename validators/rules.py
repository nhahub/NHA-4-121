# ---------------------------------------------------------------------------
# V11 - medication whitelist and trajectory rules
# ---------------------------------------------------------------------------


def validate_v11_medication_whitelist_and_trajectory(patient: PatientRecord) -> list[ValidationIssue]:
    patient_id = _patient_id(patient)
    issues: list[ValidationIssue] = []
    conditions = set(_conditions(patient))
    seen_by_medication: dict[str, list[tuple[int, str, str]]] = {}

    for visit_index, visit in enumerate(_visits(patient)):
        visit_role = str(visit.get("visit_role", ""))
        visit_type = str(visit.get("visit_type", ""))
        event_type = ""
        if isinstance(visit.get("clinical_event"), Mapping):
            event_type = str(visit.get("clinical_event", {}).get("event_type", ""))

        for med_index, medication in enumerate(_medications(visit)):
            med_path = f"visits[{visit_index}].medications[{med_index}]"
            name = medication.get("medication_name")
            status = medication.get("medication_status")
            trajectory = medication.get("trajectory_event")
            frequency = medication.get("frequency")
            route = medication.get("route")

            if name not in MEDICATION_NAMES:
                issues.append(_issue(
                    "V11",
                    FAIL,
                    patient_id,
                    "Medication must be in MEDICATION_WHITELIST.",
                    f"{med_path}.medication_name",
                    value=name,
                    allowed=MEDICATION_NAMES,
                ))
                continue

            whitelist_entry = MEDICATION_WHITELIST[str(name)]

            expected_class = whitelist_entry.get("medication_class")
            if medication.get("medication_class") != expected_class:
                issues.append(_issue(
                    "V11",
                    FAIL,
                    patient_id,
                    "Medication class must match MEDICATION_WHITELIST.",
                    f"{med_path}.medication_class",
                    medication_name=name,
                    value=medication.get("medication_class"),
                    expected=expected_class,
                ))

            expected_frequency = whitelist_entry.get("frequency")
            if frequency not in FREQUENCIES or frequency != expected_frequency:
                issues.append(_issue(
                    "V11",
                    FAIL,
                    patient_id,
                    "Medication frequency must be allowed and match whitelist default.",
                    f"{med_path}.frequency",
                    medication_name=name,
                    value=frequency,
                    expected=expected_frequency,
                ))

            expected_route = whitelist_entry.get("route")
            if route not in ROUTES or route != expected_route:
                issues.append(_issue(
                    "V11",
                    FAIL,
                    patient_id,
                    "Medication route must be allowed and match whitelist default.",
                    f"{med_path}.route",
                    medication_name=name,
                    value=route,
                    expected=expected_route,
                ))

            med_condition = whitelist_entry.get("condition")
            if med_condition and med_condition not in conditions:
                issues.append(_issue(
                    "V11",
                    FAIL,
                    patient_id,
                    "Medication condition must be present in patient.conditions.",
                    f"{med_path}.medication_name",
                    medication_name=name,
                    medication_condition=med_condition,
                    patient_conditions=sorted(conditions),
                ))

            if status not in MEDICATION_STATUS:
                issues.append(_issue("V11", FAIL, patient_id, "Invalid medication_status.", f"{med_path}.medication_status", value=status, allowed=MEDICATION_STATUS))

            if trajectory not in MEDICATION_TRAJECTORY_EVENTS:
                issues.append(_issue("V11", FAIL, patient_id, "Invalid trajectory_event.", f"{med_path}.trajectory_event", value=trajectory, allowed=MEDICATION_TRAJECTORY_EVENTS))

            # adherence_interruption is valid only for the condition-specific
            # adherence-target medication.  In the v1.7 Lite dataset:
            #   - T2DM patients → only Metformin may carry adherence_interruption
            #   - IDA  patients → only Ferrous sulfate may carry adherence_interruption
            # Other initial medications (e.g. Amlodipine for HTN in PAT-CHR-001)
            # must use simple_start_continue at adherence visits.
            if trajectory == "adherence_interruption" and not _is_adherence_target(
                medication_name=str(name), conditions=conditions
            ):
                issues.append(_issue(
                    "V11",
                    FAIL,
                    patient_id,
                    "adherence_interruption is only valid for the condition-specific "
                    "adherence target (Metformin for T2DM, Ferrous sulfate for IDA).",
                    f"{med_path}.trajectory_event",
                    medication_name=name,
                    patient_conditions=sorted(conditions),
                ))

            if status == "completed" and name not in SHORT_COURSE_MEDICATIONS:
                issues.append(_issue(
                    "V11",
                    FAIL,
                    patient_id,
                    "completed status is reserved for short-course medications.",
                    f"{med_path}.medication_status",
                    medication_name=name,
                    short_course_medications=SHORT_COURSE_MEDICATIONS,
                ))

            if trajectory == "course_completed" and name not in SHORT_COURSE_MEDICATIONS:
                issues.append(_issue(
                    "V11",
                    FAIL,
                    patient_id,
                    "course_completed trajectory is reserved for short-course medications.",
                    f"{med_path}.trajectory_event",
                    medication_name=name,
                ))

            if trajectory == "dose_adjustment" and status not in {"dose_adjusted", "added"}:
                issues.append(_issue(
                    "V11",
                    FAIL,
                    patient_id,
                    "dose_adjustment trajectory should use dose_adjusted status, or added when a controller medication is newly introduced.",
                    f"{med_path}.trajectory_event",
                    status=status,
                    trajectory_event=trajectory,
                ))

            if trajectory == "second_medication_added" and status != "added":
                issues.append(_issue(
                    "V11",
                    FAIL,
                    patient_id,
                    "second_medication_added trajectory should use added status.",
                    f"{med_path}.trajectory_event",
                    status=status,
                    trajectory_event=trajectory,
                ))

            if trajectory == "post_discharge_reconciliation" and not _has_post_discharge_context(visit_role, visit_type, event_type):
                issues.append(_issue(
                    "V11",
                    FAIL,
                    patient_id,
                    "post_discharge_reconciliation requires hospitalization or post-discharge context.",
                    f"{med_path}.trajectory_event",
                    visit_role=visit_role,
                    visit_type=visit_type,
                    event_type=event_type,
                ))

            start_date = _parse_date(medication.get("start_date"))
            stop_date = _parse_date(medication.get("stop_date")) if medication.get("stop_date") is not None else None
            if start_date is not None and stop_date is not None and stop_date < start_date:
                issues.append(_issue(
                    "V11",
                    FAIL,
                    patient_id,
                    "Medication stop_date must not be earlier than start_date.",
                    f"{med_path}.stop_date",
                    start_date=medication.get("start_date"),
                    stop_date=medication.get("stop_date"),
                ))

            seen_by_medication.setdefault(str(name), []).append((visit_index, str(status), str(trajectory)))

    for medication_name, entries in seen_by_medication.items():
        prior_statuses: set[str] = set()
        was_active = False
        for visit_index, status, trajectory in entries:
            if status == "restarted" and "temporarily_stopped" not in prior_statuses:
                issues.append(_issue(
                    "V11",
                    FAIL,
                    patient_id,
                    "restarted requires a prior temporarily_stopped event.",
                    f"visits[{visit_index}].medications[].medication_status",
                    medication_name=medication_name,
                ))
            if status == "added" and was_active:
                issues.append(_issue(
                    "V11",
                    FAIL,
                    patient_id,
                    "added should be used only before the medication was previously active.",
                    f"visits[{visit_index}].medications[].medication_status",
                    medication_name=medication_name,
                ))
            prior_statuses.add(status)
            if status in {"started", "continued", "dose_adjusted", "restarted", "added"}:
                was_active = True
            if status in {"temporarily_stopped", "completed", "stopped"}:
                was_active = False

    return issues


# ---------------------------------------------------------------------------
# V12 - dataset diversity
# ---------------------------------------------------------------------------


def validate_v12_dataset_diversity(
    patients: Sequence[PatientRecord] | PatientRecord,
    *,
    strict: bool = True,
) -> list[ValidationIssue]:
    """Validate v1.7 Lite retrieval diversity across the dataset.

    During active development, callers may set strict=False to downgrade
    duplicate-fingerprint findings to WARN.  Before final ingestion, keep
    strict=True so diversity issues block the pipeline.
    """

    normalized_patients = _normalize_patients(patients)
    severity = FAIL if strict else WARN
    issues: list[ValidationIssue] = []
    signatures: dict[str, str] = {}
    fingerprints: dict[tuple[Any, ...], str] = {}
    condition_focus_timeline: dict[tuple[str, str, str], str] = {}

    issues.extend(validate_v6_duplicate_visit_ids_dataset(normalized_patients))

    for patient in normalized_patients:
        patient_id = _patient_id(patient)
        metadata = patient.get("metadata", {}) if isinstance(patient.get("metadata"), Mapping) else {}
        conditions = _conditions(patient)
        semantic_focus = metadata.get("semantic_focus")
        timeline_pattern = metadata.get("timeline_pattern")
        retrieval_signature = metadata.get("retrieval_signature")
        retrieval_intent_tags = _as_list(metadata.get("retrieval_intent_tags"))

        if not retrieval_signature:
            issues.append(_issue("V12", severity, patient_id, "Patient must have retrieval_signature.", "metadata.retrieval_signature"))
        elif retrieval_signature in signatures:
            issues.append(_issue(
                "V12",
                severity,
                patient_id,
                "Duplicate retrieval_signature across patients.",
                "metadata.retrieval_signature",
                retrieval_signature=retrieval_signature,
                first_patient_id=signatures[str(retrieval_signature)],
            ))
        else:
            signatures[str(retrieval_signature)] = patient_id

        if len(retrieval_intent_tags) < MIN_RETRIEVAL_INTENT_TAGS or len(retrieval_intent_tags) > MAX_RETRIEVAL_INTENT_TAGS:
            issues.append(_issue(
                "V12",
                severity,
                patient_id,
                f"Each patient must have {MIN_RETRIEVAL_INTENT_TAGS}-{MAX_RETRIEVAL_INTENT_TAGS} retrieval_intent_tags.",
                "metadata.retrieval_intent_tags",
                count=len(retrieval_intent_tags),
                tags=retrieval_intent_tags,
            ))

        for tag_index, tag in enumerate(retrieval_intent_tags):
            if tag not in RETRIEVAL_INTENT_TAGS:
                issues.append(_issue(
                    "V12",
                    severity,
                    patient_id,
                    "retrieval_intent_tag must be from controlled list.",
                    f"metadata.retrieval_intent_tags[{tag_index}]",
                    tag=tag,
                ))

        for visit_index, visit in enumerate(_visits(patient)):
            if not visit.get("visit_role"):
                issues.append(_issue("V12", severity, patient_id, "Every visit must have visit_role.", f"visits[{visit_index}].visit_role"))
            if not isinstance(visit.get("clinical_event"), Mapping) or not visit.get("clinical_event"):
                issues.append(_issue("V12", severity, patient_id, "Every visit must have clinical_event.", f"visits[{visit_index}].clinical_event"))
            if not isinstance(visit.get("retrieval_context"), Mapping) or not visit.get("retrieval_context"):
                issues.append(_issue("V12", severity, patient_id, "Every visit must have retrieval_context.", f"visits[{visit_index}].retrieval_context"))

        fingerprint = build_diversity_fingerprint(patient)
        if fingerprint in fingerprints:
            issues.append(_issue(
                "V12",
                severity,
                patient_id,
                "Duplicate diversity fingerprint across patients.",
                "metadata",
                first_patient_id=fingerprints[fingerprint],
                fingerprint=repr(fingerprint),
            ))
        else:
            fingerprints[fingerprint] = patient_id

        for condition in conditions:
            key = (condition, str(semantic_focus), str(timeline_pattern))
            if key in condition_focus_timeline and condition_focus_timeline[key] != patient_id:
                issues.append(_issue(
                    "V12",
                    severity,
                    patient_id,
                    "Patients with overlapping primary conditions should not share both semantic_focus and timeline_pattern.",
                    "metadata.semantic_focus",
                    condition=condition,
                    semantic_focus=semantic_focus,
                    timeline_pattern=timeline_pattern,
                    first_patient_id=condition_focus_timeline[key],
                ))
            else:
                condition_focus_timeline[key] = patient_id

    return issues


def build_diversity_fingerprint(patient: PatientRecord) -> tuple[Any, ...]:
    """Build the v1.7 Lite diversity fingerprint for one patient."""

    metadata = patient.get("metadata", {}) if isinstance(patient.get("metadata"), Mapping) else {}
    visit_roles = tuple(str(visit.get("visit_role", "")) for visit in _visits(patient))
    main_medications = tuple(sorted({
        str(medication.get("medication_name"))
        for visit in _visits(patient)
        for medication in _medications(visit)
        if medication.get("medication_name")
    }))
    lab_focus = tuple(sorted({
        str(lab.get("lab_type"))
        for visit in _visits(patient)
        for lab in _labs(visit)
        if lab.get("lab_type")
    }))
    allergy_allergen = tuple(sorted({
        str(allergy.get("allergen"))
        for allergy in _allergies(patient)
        if allergy.get("allergen")
    }))

    return (
        metadata.get("tier"),
        tuple(sorted(_conditions(patient))),
        metadata.get("semantic_focus"),
        metadata.get("timeline_pattern"),
        visit_roles,
        main_medications,
        lab_focus,
        allergy_allergen,
    )


