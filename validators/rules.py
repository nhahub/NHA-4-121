def _as_list(value: Any) -> list[Any]:
    if value is None:
        return []
    if isinstance(value, (str, bytes)):
        return [value]
    if isinstance(value, Sequence):
        return list(value)
    return [value]


def _require_fields(
    obj: Mapping[str, Any],
    required_fields: Iterable[str],
    patient_id: str,
    rule_id: str,
    base_path: str,
) -> list[ValidationIssue]:
    issues: list[ValidationIssue] = []
    for field_name in required_fields:
        if field_name not in obj:
            path = f"{base_path}.{field_name}" if base_path else field_name
            issues.append(_issue(
                rule_id,
                FAIL,
                patient_id,
                f"Required field missing: {field_name}.",
                path,
                field=field_name,
            ))
    return issues


def _issue(
    rule_id: str,
    severity: ValidationSeverity,
    patient_id: str,
    message: str,
    path: str = "",
    **context: Any,
) -> ValidationIssue:
    return ValidationIssue(
        rule_id=rule_id,
        severity=severity,
        patient_id=patient_id,
        message=message,
        path=path,
        context=context,
    )


def _parse_date(value: Any) -> date | None:
    if not isinstance(value, str):
        return None
    if not _matches_regex(value, DATE_REGEX):
        return None
    try:
        return datetime.strptime(value, DATE_FORMAT).date()
    except ValueError:
        return None


def _calculate_age(dob: date, *, reference_date: date | None = None) -> int:
    ref = reference_date or date.today()
    return ref.year - dob.year - ((ref.month, ref.day) < (dob.month, dob.day))


def _matches_regex(value: str, pattern: str) -> bool:
    return re.match(pattern, str(value)) is not None


def _normalize_text(value: Any) -> str:
    return str(value or "").strip().casefold()


def _as_float(value: Any) -> float | None:
    try:
        numeric = float(value)
    except (TypeError, ValueError):
        return None
    if math.isnan(numeric) or math.isinf(numeric):
        return None
    return numeric


def _contains_forbidden_term(text: str, forbidden_terms: Iterable[str]) -> bool:
    normalized = _normalize_text(text)
    return any(_normalize_text(term) in normalized for term in forbidden_terms)


def _has_post_discharge_context(visit_role: str, visit_type: str, event_type: str) -> bool:
    values = {visit_role, visit_type, event_type}
    return bool(
        values
        & {
            "hospitalization",
            "post_discharge_stabilization",
            "post_discharge_review",
            "medication_reconciliation",
        }
    )


def _is_adherence_target(medication_name: str, conditions: set[str]) -> bool:
    """Return True when medication_name is the documented adherence target
    for this patient's conditions.

    adherence_interruption must not be applied broadly to every initial
    medication at an adherence visit — only the condition-specific adherence
    target should carry this trajectory.  For the v1.7 Lite dataset:
      - T2DM → Metformin (missed diabetes medication doses)
      - IDA  → Ferrous sulfate (missed iron supplement doses)
    All other medications at adherence visits must use simple_start_continue.
    """
    if "T2DM" in conditions and medication_name == "Metformin":
        return True
    if "IDA" in conditions and medication_name == "Ferrous sulfate":
        return True
    return False


def _walk_mapping(mapping: Mapping[str, Any], *, base_path: str) -> Iterable[tuple[str, str, Any]]:
    for key, value in mapping.items():
        path = f"{base_path}.{key}" if base_path else str(key)
        yield path, str(key), value
        if isinstance(value, Mapping):
            yield from _walk_mapping(value, base_path=path)
        elif isinstance(value, Sequence) and not isinstance(value, (str, bytes)):
            for index, item in enumerate(value):
                item_path = f"{path}[{index}]"
                if isinstance(item, Mapping):
                    yield from _walk_mapping(item, base_path=item_path)
                else:
                    yield item_path, str(key), item


def _find_key_recursively(value: Any, key_to_find: str, *, path: str = "") -> Iterable[tuple[str, Any]]:
    if isinstance(value, Mapping):
        for key, child in value.items():
            child_path = f"{path}.{key}" if path else str(key)
            if key == key_to_find:
                yield child_path, child
            yield from _find_key_recursively(child, key_to_find, path=child_path)
    elif isinstance(value, Sequence) and not isinstance(value, (str, bytes)):
        for index, child in enumerate(value):
            child_path = f"{path}[{index}]" if path else f"[{index}]"
            yield from _find_key_recursively(child, key_to_find, path=child_path)


