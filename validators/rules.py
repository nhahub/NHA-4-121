# V5 - prior visit linkage
# ---------------------------------------------------------------------------


def validate_v5_prior_visit_integrity(patient: PatientRecord) -> list[ValidationIssue]:
    patient_id = _patient_id(patient)
    issues: list[ValidationIssue] = []
    visits = _visits(patient)
    known_visit_ids = {str(visit.get("visit_id")) for visit in visits if visit.get("visit_id") is not None}

    for index, visit in enumerate(visits):
        prior = visit.get("prior_visit_id")
        if index == 0:
            if prior is not None:
                issues.append(_issue(
                    "V5",
                    WARN,
                    patient_id,
                    "First visit should not have prior_visit_id.",
                    f"visits[{index}].prior_visit_id",
                    prior_visit_id=prior,
                ))
            continue

        previous_visit_id = visits[index - 1].get("visit_id")
        if prior is None:
            issues.append(_issue(
                "V5",
                WARN,
                patient_id,
                "Follow-up visit should reference the previous visit.",
                f"visits[{index}].prior_visit_id",
                expected=previous_visit_id,
            ))
        elif str(prior) not in known_visit_ids:
            issues.append(_issue(
                "V5",
                WARN,
                patient_id,
                "prior_visit_id must reference an existing visit_id.",
                f"visits[{index}].prior_visit_id",
                prior_visit_id=prior,
            ))
        elif prior != previous_visit_id:
            issues.append(_issue(
                "V5",
                WARN,
                patient_id,
                "prior_visit_id should point to the immediately previous visit for deterministic timelines.",
                f"visits[{index}].prior_visit_id",
                actual=prior,
                expected=previous_visit_id,
            ))

    return issues


