# V1 - chronology
# ---------------------------------------------------------------------------


def validate_v1_chronological_visit_order(patient: PatientRecord) -> list[ValidationIssue]:
    patient_id = _patient_id(patient)
    issues: list[ValidationIssue] = []
    previous_date: date | None = None

    for index, visit in enumerate(_visits(patient)):
        path = f"visits[{index}].visit_date"
        visit_date_raw = visit.get("visit_date")
        parsed = _parse_date(visit_date_raw)
        if parsed is None:
            # V8 owns date-format failure.  Avoid duplicate noise here.
            continue
        if previous_date is not None and parsed < previous_date:
            issues.append(_issue(
                "V1",
                FAIL,
                patient_id,
                "Visit dates must be in chronological order.",
                path,
                previous_date=str(previous_date),
                current_date=str(parsed),
            ))
        previous_date = parsed

    return issues


# ---------------------------------------------------------------------------
# V2 - allergy contradiction
# ---------------------------------------------------------------------------


def validate_v2_allergy_medication_contradiction(patient: PatientRecord) -> list[ValidationIssue]:
    patient_id = _patient_id(patient)
    issues: list[ValidationIssue] = []
    allergens = {_normalize_text(item.get("allergen")) for item in _allergies(patient)}
    allergens.discard("")

    if not allergens:
        return issues

    for visit_index, visit in enumerate(_visits(patient)):
        for med_index, medication in enumerate(_medications(visit)):
            med_name = _normalize_text(medication.get("medication_name"))
            if med_name and med_name in allergens:
                issues.append(_issue(
                    "V2",
                    FAIL,
                    patient_id,
                    "Medication name must not match recorded allergy allergen.",
                    f"visits[{visit_index}].medications[{med_index}].medication_name",
                    medication_name=medication.get("medication_name"),
                    allergen=medication.get("medication_name"),
                ))

    return issues


