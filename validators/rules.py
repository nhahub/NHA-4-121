PatientRecord = dict[str, Any]
VisitRecord = dict[str, Any]
ValidationSeverity = str

FAIL: ValidationSeverity = "FAIL"
WARN: ValidationSeverity = "WARN"
INFO: ValidationSeverity = "INFO"
REPORT: ValidationSeverity = "REPORT"


@dataclass(frozen=True)
class ValidationIssue:
    """One validation finding produced by a rule."""

    rule_id: str
    severity: ValidationSeverity
    patient_id: str
    message: str
    path: str = ""
    context: dict[str, Any] = field(default_factory=dict)

    def as_dict(self) -> dict[str, Any]:
        return {
            "rule_id": self.rule_id,
            "severity": self.severity,
            "patient_id": self.patient_id,
            "path": self.path,
            "message": self.message,
            "context": dict(self.context),
        }


@dataclass(frozen=True)
class ValidationSummary:
    """Simple in-memory summary returned by run_all_rules()."""

    issues: tuple[ValidationIssue, ...]

    @property
    def fail_count(self) -> int:
        return sum(1 for issue in self.issues if issue.severity == FAIL)

    @property
    def warn_count(self) -> int:
        return sum(1 for issue in self.issues if issue.severity == WARN)

    @property
    def info_count(self) -> int:
        return sum(1 for issue in self.issues if issue.severity in {INFO, REPORT})

    @property
    def passed(self) -> bool:
        return self.fail_count == 0

    def as_dict(self) -> dict[str, Any]:
        return {
            "passed": self.passed,
            "fail_count": self.fail_count,
            "warn_count": self.warn_count,
            "info_count": self.info_count,
            "issues": [issue.as_dict() for issue in self.issues],
        }


