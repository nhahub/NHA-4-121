def validate_v8_date_formats(patient: PatientRecord) -> list[ValidationIssue]:
    patient_id = _patient_id(patient)
    issues: list[ValidationIssue] = []

    date_fields: list[tuple[str, Any]] = []
    demographics = patient.get("demographics", {}) if isinstance(patient.get("demographics"), Mapping) else {}
    date_fields.append(("demographics.date_of_birth", demographics.get("date_of_birth")))

