# V7 - enums and CKD rule
# ---------------------------------------------------------------------------


def validate_v7_enums_and_ckd_rule(patient: PatientRecord) -> list[ValidationIssue]:
    patient_id = _patient_id(patient)
    issues: list[ValidationIssue] = []
    metadata = patient.get("metadata", {}) if isinstance(patient.get("metadata"), Mapping) else {}
    conditions = _conditions(patient)
    tier = metadata.get("tier")

    if not _matches_regex(patient_id, PATIENT_ID_REGEX):
        issues.append(_issue("V7", FAIL, patient_id, "Invalid patient_id format.", "patient_id", value=patient_id))

    for cond_index, condition in enumerate(conditions):
        if condition not in CONDITIONS:
            issues.append(_issue(
                "V7",
                FAIL,
                patient_id,
                "Invalid condition enum value.",
                f"conditions[{cond_index}]",
                value=condition,
                allowed=CONDITIONS,
            ))

    if tier not in TIERS:
        issues.append(_issue("V7", FAIL, patient_id, "Invalid tier enum value.", "metadata.tier", value=tier, allowed=TIERS))

    sex = patient.get("demographics", {}).get("sex") if isinstance(patient.get("demographics"), Mapping) else None
    if sex not in SEX_VALUES:
        issues.append(_issue("V7", FAIL, patient_id, "Invalid sex enum value.", "demographics.sex", value=sex, allowed=SEX_VALUES))

    semantic_focus = metadata.get("semantic_focus")
    if semantic_focus not in SEMANTIC_FOCUS:
        issues.append(_issue(
            "V7",
            FAIL,
            patient_id,
            "Invalid semantic_focus enum value.",
            "metadata.semantic_focus",
            value=semantic_focus,
            allowed=SEMANTIC_FOCUS,
        ))

    timeline_pattern = metadata.get("timeline_pattern")
    if timeline_pattern not in TIMELINE_PATTERNS:
        issues.append(_issue(
            "V7",
            FAIL,
            patient_id,
            "Invalid timeline_pattern enum value.",
            "metadata.timeline_pattern",
            value=timeline_pattern,
            allowed=TIMELINE_PATTERNS,
        ))

    soap_style = metadata.get("soap_style")
    if soap_style not in SOAP_STYLES:
        issues.append(_issue("V7", FAIL, patient_id, "Invalid soap_style enum value.", "metadata.soap_style", value=soap_style, allowed=SOAP_STYLES))

    for tag_index, tag in enumerate(_as_list(metadata.get("retrieval_intent_tags"))):
        if tag not in RETRIEVAL_INTENT_TAGS:
            issues.append(_issue(
                "V7",
                FAIL,
                patient_id,
                "Invalid retrieval_intent_tag value.",
                f"metadata.retrieval_intent_tags[{tag_index}]",
                value=tag,
                allowed=RETRIEVAL_INTENT_TAGS,
            ))

    for target_index, target in enumerate(_as_list(metadata.get("primary_retrieval_targets"))):
        if target not in PRIMARY_RETRIEVAL_TARGETS:
            issues.append(_issue(
                "V7",
                FAIL,
                patient_id,
                "Invalid primary_retrieval_target value.",
                f"metadata.primary_retrieval_targets[{target_index}]",
                value=target,
                allowed=PRIMARY_RETRIEVAL_TARGETS,
            ))

    if "CKD" in conditions:
        missing_required = [condition for condition in CKD_REQUIRED_CONDITIONS if condition not in conditions]
        if missing_required or tier != CKD_ALLOWED_TIER:
            issues.append(_issue(
                "V7",
                FAIL,
                patient_id,
                "CKD requires chronic tier and co-occurring T2DM + HTN.",
                "conditions",
                conditions=conditions,
                tier=tier,
                missing_required_conditions=missing_required,
            ))

    for visit_index, visit in enumerate(_visits(patient)):
        visit_path = f"visits[{visit_index}]"
        visit_id = str(visit.get("visit_id", ""))
        if visit_id and not _matches_regex(visit_id, VISIT_ID_REGEX):
            issues.append(_issue("V7", FAIL, patient_id, "Invalid visit_id format.", f"{visit_path}.visit_id", value=visit_id))

        visit_type = visit.get("visit_type")
        if visit_type not in VISIT_TYPES:
            issues.append(_issue("V7", FAIL, patient_id, "Invalid visit_type enum value.", f"{visit_path}.visit_type", value=visit_type, allowed=VISIT_TYPES))

        visit_role = visit.get("visit_role")
        if visit_role not in VISIT_ROLES:
            issues.append(_issue("V7", FAIL, patient_id, "Invalid visit_role enum value.", f"{visit_path}.visit_role", value=visit_role, allowed=VISIT_ROLES))

        visit_timeline = visit.get("timeline_pattern")
        if visit_timeline not in TIMELINE_PATTERNS:
            issues.append(_issue("V7", FAIL, patient_id, "Invalid visit timeline_pattern.", f"{visit_path}.timeline_pattern", value=visit_timeline, allowed=TIMELINE_PATTERNS))
        elif timeline_pattern in TIMELINE_PATTERNS and visit_timeline != timeline_pattern:
            issues.append(_issue(
                "V7",
                FAIL,
                patient_id,
                "Visit timeline_pattern must match patient metadata timeline_pattern.",
                f"{visit_path}.timeline_pattern",
                visit_value=visit_timeline,
                patient_value=timeline_pattern,
            ))

        for diag_index, diagnosis in enumerate(_as_list(visit.get("diagnoses"))):
            if diagnosis not in CONDITIONS:
                issues.append(_issue("V7", FAIL, patient_id, "Invalid diagnosis enum value.", f"{visit_path}.diagnoses[{diag_index}]", value=diagnosis, allowed=CONDITIONS))
            elif diagnosis not in conditions:
                issues.append(_issue(
                    "V7",
                    FAIL,
                    patient_id,
                    "Visit diagnosis must be present in patient.conditions.",
                    f"{visit_path}.diagnoses[{diag_index}]",
                    diagnosis=diagnosis,
                    patient_conditions=conditions,
                ))

        clinical_event = visit.get("clinical_event", {}) if isinstance(visit.get("clinical_event"), Mapping) else {}
        event_type = clinical_event.get("event_type")
        if event_type not in CLINICAL_EVENT_TYPES:
            issues.append(_issue("V7", FAIL, patient_id, "Invalid clinical_event.event_type.", f"{visit_path}.clinical_event.event_type", value=event_type, allowed=CLINICAL_EVENT_TYPES))

        retrieval_context = visit.get("retrieval_context", {}) if isinstance(visit.get("retrieval_context"), Mapping) else {}
        if retrieval_context.get("semantic_focus") not in SEMANTIC_FOCUS:
            issues.append(_issue("V7", FAIL, patient_id, "Invalid retrieval_context.semantic_focus.", f"{visit_path}.retrieval_context.semantic_focus", value=retrieval_context.get("semantic_focus"), allowed=SEMANTIC_FOCUS))

        for tag_index, tag in enumerate(_as_list(retrieval_context.get("retrieval_intent_tags"))):
            if tag not in RETRIEVAL_INTENT_TAGS:
                issues.append(_issue("V7", FAIL, patient_id, "Invalid retrieval_context tag.", f"{visit_path}.retrieval_context.retrieval_intent_tags[{tag_index}]", value=tag, allowed=RETRIEVAL_INTENT_TAGS))

        for lab_index, lab in enumerate(_labs(visit)):
            lab_type = lab.get("lab_type")
            if lab_type not in LAB_TYPES:
                issues.append(_issue("V7", FAIL, patient_id, "Invalid lab_type enum value.", f"{visit_path}.labs[{lab_index}].lab_type", value=lab_type, allowed=LAB_TYPES))
            flag = lab.get("flag")
            if flag not in FLAGS:
                issues.append(_issue("V7", FAIL, patient_id, "Invalid lab flag enum value.", f"{visit_path}.labs[{lab_index}].flag", value=flag, allowed=FLAGS))

        for med_index, medication in enumerate(_medications(visit)):
            frequency = medication.get("frequency")
            route = medication.get("route")
            medication_status = medication.get("medication_status")
            trajectory_event = medication.get("trajectory_event")
            if frequency not in FREQUENCIES:
                issues.append(_issue("V7", FAIL, patient_id, "Invalid medication frequency enum value.", f"{visit_path}.medications[{med_index}].frequency", value=frequency, allowed=FREQUENCIES))
            if route not in ROUTES:
                issues.append(_issue("V7", FAIL, patient_id, "Invalid medication route enum value.", f"{visit_path}.medications[{med_index}].route", value=route, allowed=ROUTES))
            if medication_status not in MEDICATION_STATUS:
                issues.append(_issue("V7", FAIL, patient_id, "Invalid medication_status enum value.", f"{visit_path}.medications[{med_index}].medication_status", value=medication_status, allowed=MEDICATION_STATUS))
            if trajectory_event not in MEDICATION_TRAJECTORY_EVENTS:
                issues.append(_issue("V7", FAIL, patient_id, "Invalid trajectory_event enum value.", f"{visit_path}.medications[{med_index}].trajectory_event", value=trajectory_event, allowed=MEDICATION_TRAJECTORY_EVENTS))

    for allergy_index, allergy in enumerate(_allergies(patient)):
        severity = allergy.get("severity")
        if severity not in SEVERITIES:
            issues.append(_issue("V7", FAIL, patient_id, "Invalid allergy severity enum value.", f"allergy_registry[{allergy_index}].severity", value=severity, allowed=SEVERITIES))

    return issues


