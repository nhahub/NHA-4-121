# Public runner
# ---------------------------------------------------------------------------


def run_all_rules(
    patients: Sequence[PatientRecord] | PatientRecord,
    *,
    strict_diversity: bool = True,
) -> ValidationSummary:
    """Run V1-V12 validation over one patient or a dataset.

    V13 is intentionally not included here because it needs chunks after SOAP
    and chunk dry-run.  Use run_v13_similarity_report(chunks) separately.
    """

    normalized_patients = _normalize_patients(patients)

    issues: list[ValidationIssue] = []
    per_patient_rules = (
        validate_v1_chronological_visit_order,
        validate_v2_allergy_medication_contradiction,
        validate_v3_vitals_and_age_bounds,
        validate_v4_required_fields,
        validate_v5_prior_visit_integrity,
        validate_v6_duplicate_visit_ids,
        validate_v7_enums_and_ckd_rule,
        validate_v8_date_formats,
        validate_v9_bp_forbidden_in_labs_and_metadata,
        validate_v10_timeline_events_forbidden,
        validate_v11_medication_whitelist_and_trajectory,
    )

    for patient in normalized_patients:
        for rule in per_patient_rules:
            issues.extend(rule(patient))

    issues.extend(validate_v12_dataset_diversity(normalized_patients, strict=strict_diversity))
    return ValidationSummary(tuple(issues))


