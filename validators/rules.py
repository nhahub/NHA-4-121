# V3 - vitals and age bounds
# ---------------------------------------------------------------------------


def validate_v3_vitals_and_age_bounds(patient: PatientRecord) -> list[ValidationIssue]:
    patient_id = _patient_id(patient)
    issues: list[ValidationIssue] = []

    dob = _parse_date(patient.get("demographics", {}).get("date_of_birth"))
    if dob is not None:
        age = _calculate_age(dob)
        min_age, max_age = AGE_LIMITS
        if age < min_age or age > max_age:
            issues.append(_issue(
                "V3",
                FAIL,
                patient_id,
                f"Patient age must be between {min_age} and {max_age}.",
                "demographics.date_of_birth",
                age=age,
                date_of_birth=str(dob),
            ))

    for visit_index, visit in enumerate(_visits(patient)):
        vitals = visit.get("vitals", {})
        if not isinstance(vitals, Mapping):
            issues.append(_issue(
                "V3",
                FAIL,
                patient_id,
                "vitals must be an object.",
                f"visits[{visit_index}].vitals",
            ))
            continue

        for field_name, (lower, upper) in _vital_limits().items():
            value = vitals.get(field_name)
            if value is None:
                # V4 owns missing required vital fields.
                continue
            numeric = _as_float(value)
            if numeric is None or numeric < lower or numeric > upper:
                issues.append(_issue(
                    "V3",
                    FAIL,
                    patient_id,
                    f"Vital {field_name} must be numeric and between {lower} and {upper}.",
                    f"visits[{visit_index}].vitals.{field_name}",
                    value=value,
                    min=lower,
                    max=upper,
                ))

    return issues


def _vital_limits() -> dict[str, tuple[float, float]]:
    from config.constants import VITAL_LIMITS

    return dict(VITAL_LIMITS)


