def validate_v6_duplicate_visit_ids(patient: PatientRecord) -> list[ValidationIssue]:
    patient_id = _patient_id(patient)
    issues: list[ValidationIssue] = []
    seen: dict[str, int] = {}

    for index, visit in enumerate(_visits(patient)):
        visit_id = str(visit.get("visit_id", ""))
        if not visit_id:
            continue
        if visit_id in seen:
            issues.append(_issue(
                "V6",
                FAIL,
                patient_id,
                "Duplicate visit_id inside patient record.",
                f"visits[{index}].visit_id",
                duplicate_visit_id=visit_id,
                first_index=seen[visit_id],
                duplicate_index=index,
            ))
        else:
            seen[visit_id] = index

    return issues


def validate_v6_duplicate_visit_ids_dataset(patients: Sequence[PatientRecord]) -> list[ValidationIssue]:
    """Optional dataset-level duplicate visit_id check."""

    issues: list[ValidationIssue] = []
    seen: dict[str, str] = {}
    for patient in patients:
        patient_id = _patient_id(patient)
        for visit in _visits(patient):
            visit_id = str(visit.get("visit_id", ""))
            if not visit_id:
                continue
            if visit_id in seen and seen[visit_id] != patient_id:
                issues.append(_issue(
                    "V6",
                    FAIL,
                    patient_id,
                    "Duplicate visit_id across dataset.",
                    "visits[].visit_id",
                    duplicate_visit_id=visit_id,
                    first_patient_id=seen[visit_id],
                    duplicate_patient_id=patient_id,
                ))
            else:
                seen[visit_id] = patient_id
    return issues


