# Metadata contract helper for Gamal / ingestion tests
# ---------------------------------------------------------------------------


def validate_chroma_metadata_contract(
    metadata: Mapping[str, Any],
    *,
    patient_id: str = "UNKNOWN",
    source_type_required: bool = True,
) -> list[ValidationIssue]:
    """Validate minimal ChromaDB metadata without calling ChromaDB.

    This helper is intentionally placed here so tests can reuse the same safety
    rules while metadata_builder.py still owns actual metadata construction.
    """

    issues: list[ValidationIssue] = []
    allowed_fields = set(MINIMAL_CHROMA_METADATA_FIELDS_V17_LITE) | set(OPTIONAL_CHROMA_METADATA_FIELDS_V17_LITE)

    for required in MINIMAL_CHROMA_METADATA_FIELDS_V17_LITE:
        if required not in metadata:
            if required == "source_type" and not source_type_required:
                continue
            issues.append(_issue(
                "METADATA",
                FAIL,
                patient_id,
                "Required Chroma metadata field missing.",
                f"metadata.{required}",
                field=required,
            ))

    for key in metadata.keys():
        if key in FORBIDDEN_CHROMA_METADATA_FIELDS_V17_LITE:
            issues.append(_issue(
                "METADATA",
                FAIL,
                patient_id,
                "Forbidden Chroma metadata field present.",
                f"metadata.{key}",
                field=key,
            ))
        if key not in allowed_fields:
            issues.append(_issue(
                "METADATA",
                WARN,
                patient_id,
                "Metadata field is outside the v1.7 Lite minimal contract.",
                f"metadata.{key}",
                field=key,
            ))

    if metadata.get("source_type") is not None and metadata.get("source_type") not in SOURCE_TYPES:
        issues.append(_issue(
            "METADATA",
            FAIL,
            patient_id,
            "Invalid source_type in Chroma metadata.",
            "metadata.source_type",
            value=metadata.get("source_type"),
            allowed=SOURCE_TYPES,
        ))

    if metadata.get("visit_type") is not None and metadata.get("visit_type") not in VISIT_TYPES:
        issues.append(_issue("METADATA", FAIL, patient_id, "Invalid visit_type in metadata.", "metadata.visit_type", value=metadata.get("visit_type"), allowed=VISIT_TYPES))
    if metadata.get("visit_role") is not None and metadata.get("visit_role") not in VISIT_ROLES:
        issues.append(_issue("METADATA", FAIL, patient_id, "Invalid visit_role in metadata.", "metadata.visit_role", value=metadata.get("visit_role"), allowed=VISIT_ROLES))
    if metadata.get("semantic_focus") is not None and metadata.get("semantic_focus") not in SEMANTIC_FOCUS:
        issues.append(_issue("METADATA", FAIL, patient_id, "Invalid semantic_focus in metadata.", "metadata.semantic_focus", value=metadata.get("semantic_focus"), allowed=SEMANTIC_FOCUS))
    if metadata.get("timeline_pattern") is not None and metadata.get("timeline_pattern") not in TIMELINE_PATTERNS:
        issues.append(_issue("METADATA", FAIL, patient_id, "Invalid timeline_pattern in metadata.", "metadata.timeline_pattern", value=metadata.get("timeline_pattern"), allowed=TIMELINE_PATTERNS))

    issues.extend(validate_metadata_bp_absence(metadata, patient_id=patient_id, base_path="metadata", check_values=False))
    return issues


# ---------------------------------------------------------------------------
# V13 - report-only embedding similarity helper
# ---------------------------------------------------------------------------


def run_v13_similarity_report(
    chunks: Sequence[Mapping[str, Any]],
    *,
    model_name: str = EMBEDDING_MODEL_NAME,
    warn_threshold: float = V13_SIMILARITY_WARN_THRESHOLD,
    critical_threshold: float = V13_SIMILARITY_CRITICAL_THRESHOLD,
    source_types_to_check: set[str] | None = None,
) -> dict[str, Any]:
    """Run report-only chunk similarity audit.

    This function is intentionally optional and lazy-imports sentence_transformers.
    It should be called after SOAP generation and chunk dry-run, not during
    patient JSON validation.  It does not call ChromaDB.
    """

    if source_types_to_check is None:
        source_types_to_check = {"doctor_note", "discharge_summary", "medication_reconciliation"}

    auditable = [
        chunk
        for chunk in chunks
        if isinstance(chunk.get("metadata"), Mapping)
        and chunk.get("metadata", {}).get("source_type") in source_types_to_check
        and str(chunk.get("text", "")).strip()
    ]

    report: dict[str, Any] = {
        "rule_id": "V13",
        "status": "report_only",
        "model": model_name,
        "chunks_checked": len(auditable),
        "critical": [],
        "warnings": [],
        "informational": [],
    }

    if len(auditable) < 2:
        return report

    texts = [str(chunk["text"]) for chunk in auditable]

    try:
        from sentence_transformers import SentenceTransformer  # type: ignore
    except Exception as exc:  # pragma: no cover - environment dependent
        report["status"] = "skipped_missing_dependency"
        report["message"] = "sentence-transformers is not installed; V13 was skipped."
        report["error"] = str(exc)
        return report

    model = SentenceTransformer(model_name)
    embeddings = model.encode(texts, normalize_embeddings=True)

    for i, j in combinations(range(len(auditable)), 2):
        chunk_a = auditable[i]
        chunk_b = auditable[j]
        metadata_a = chunk_a.get("metadata", {})
        metadata_b = chunk_b.get("metadata", {})

        if metadata_a.get("visit_id") == metadata_b.get("visit_id"):
            continue

        similarity = float(embeddings[i] @ embeddings[j])
        same_patient = metadata_a.get("patient_id") == metadata_b.get("patient_id")
        same_source = metadata_a.get("source_type") == metadata_b.get("source_type")

        record = {
            "similarity": round(similarity, 4),
            "chunk_a": chunk_a.get("chunk_id"),
            "chunk_b": chunk_b.get("chunk_id"),
            "patient_a": metadata_a.get("patient_id"),
            "patient_b": metadata_b.get("patient_id"),
            "source_type_a": metadata_a.get("source_type"),
            "source_type_b": metadata_b.get("source_type"),
            "same_patient": same_patient,
            "same_source": same_source,
        }

        if not same_patient and same_source and similarity >= critical_threshold:
            report["critical"].append(record)
        elif same_source and similarity >= warn_threshold:
            report["warnings"].append(record)
        elif same_source and similarity >= 0.82:
            report["informational"].append(record)

    return report


